# Creating a list of marks
marks = [94, 95, 87, 66, 45]

# Printing the list
print(marks)

# Checking the type of the 'marks' variable (should be <class 'list'>)
print(type(marks))

# Getting the total number of elements in the list
print(len(marks))

# Accessing individual elements using index (indexing starts at 0)
print(marks[0])  # First element
print(marks[1])  # Second element
print(marks[2])  # Third element
print(marks[3])  # Fourth element
print(marks[4])  # Fifth element

# Creating a mixed-type list with student details
stud = ["Karan", 85, "Mumbai"]
print(stud)

# Modifying the first element (name) in the list
stud[0] = "Arjun"
print(stud)
