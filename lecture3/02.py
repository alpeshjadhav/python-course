s = "Python"
print(s)

# Positive indexing
print(s[0])  # 'P'
print(s[1])  # 'y'

# Negative indexing
print(s[-1])  # 'n'
print(s[-2])  # 'o'

str = "Mumbai Indians"
print(str)
print(str[4])

# Slicing
print(str[0:6])
print(str[7:len(str)])
print(str[-7:-1])
print(str[2:])
print(str[::2])
print(str[::-1])